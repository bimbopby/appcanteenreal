package Model;

/**
 *
 * @author Administrator
 */
public class NCC {
    private String id,name,sdt,diachi;

    public NCC() {
    }

    public NCC(String id, String name, String sdt, String diachi) {
        this.id = id;
        this.name = name;
        this.sdt = sdt;
        this.diachi = diachi;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }
    
    
    
    
}
